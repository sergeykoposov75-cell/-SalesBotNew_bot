def classify_intent(text: str) -> str:
    t = text.lower()
    for intent, keywords in _INTENT_KEYWORDS.items():
        if any(kw in t for kw in keywords):
            return intent
    return "other"


INTENT_NAMES: dict[str, str] = {
    "price": "цена",
    "payment": "оплата",
    "delivery": "доставка и внедрение",
    "comparison": "сравнение тарифов",
    "support": "техподдержка",
    "trial": "тестовый период",
    "specs": "характеристики",
    "contact": "связь с менеджером",
}


def search_knowledge_base(text: str) -> str | None:
    t = text.lower()
    answers: list[str] = []

    for intent, kw in _INTENT_KEYWORDS.items():
        if any(k in t for k in kw):
            if intent == "price":
                answers.append(KB["prices"])
            elif intent == "payment":
                answers.append(KB["payment_terms"])
            elif intent == "delivery":
                answers.append(KB["delivery"])
            elif intent == "comparison":
                lines = []
                for prod_name, tariffs in KB["tariffs"].items():
                    line = f"• {prod_name}: " + ", ".join(
                        f"{k} — {v}" for k, v in tariffs.items()
                    )
                    lines.append(line)
                answers.append("Сравнение тарифов:\n" + "\n".join(lines))
            elif intent == "support":
                answers.append(KB["support"])
            elif intent == "trial":
                answers.append(KB["trial"])
            elif intent == "specs":
                answers.append(
                    "Наши продукты:\n"
                    + "\n".join(f"• {p}" for p in KB["products"])
                )

    for faq in KB["faq"]:
        if faq["q"].lower()[:15] in t or t[:15] in faq["q"].lower():
            answers.append(faq["a"])

    return "\n\n".join(answers) if answers else None
